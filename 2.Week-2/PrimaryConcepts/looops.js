

// Using a for loop
console.log("Using for loop:");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// Using a while loop
console.log("\nUsing while loop:");
let j = 1;
while (j <= 5) {
    console.log(j);
    j++;
}

// Using a do...while loop
console.log("\nUsing do...while loop:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 5);
